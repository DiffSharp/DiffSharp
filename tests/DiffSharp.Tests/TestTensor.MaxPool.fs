            let tk3, tk3i = dsharp.maxpool1di(t, 3)
            let tk3Correct = combo.tensor([[[ 2.5995,  1.3858,  0.9593],
                                            [ 0.4564,  0.4587,  1.1539]],
                                     
                                           [[ 0.9980,  0.1321,  1.0608],
                                            [ 1.1872, -0.2067,  0.6039]]])
