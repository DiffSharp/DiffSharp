    [<Test>]
    member _.TestTensorMaxPool1D () =
        for combo in Combos.FloatingPointExcept16s do
            let t = combo.tensor([[[-2.1704, -1.1558,  2.5995,  1.3858, -1.3157, -0.3179,  0.9593,  -2.1432,  0.7169, -1.7999],
